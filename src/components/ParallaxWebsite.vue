<script>
import Splitting from "../js/splitting.js";
import { gsap } from "gsap";
import { ScrollTrigger } from "gsap/ScrollTrigger";
import luxy from "../js/luxy.js";
import PinnedImageVue from "./Pinnedmage.vue";

export default {
  name: "LuxyComponent",
  components: {
    PinnedImageVue,
  },
  mounted() {
    Splitting();
    luxy.init();
    gsap.registerPlugin(ScrollTrigger);

    // Başlangıç Animasyonları
    const gTl = gsap.timeline();
    gTl
      .from(".title .char", {
        opacity: 0,
        yPercent: 100,
        stagger: 0.03,
        ease: "back.out",
      })
      .from(
        ".title__exp",
        {
          opacity: 0,
          yPercent: 130,
          stagger: 0.04,
          ease: "back.out",
        },
        "-=1"
      )
      .from(
        ".header__btn",
        {
          opacity: 0,
          yPercent: 130,
          stagger: 0.04,
          ease: "back.out",
        },
        "-=1"
      )
      .to(
        ".header__img",
        {
          clipPath: "polygon(0% 0%, 100% 0%, 100% 100%, 0% 100%)",
          scale: 1,
          ease: "back.out",
          duration: 4,
        },
        "-=1"
      )

      .from(
        ".header__marq",
        {
          opacity: 0,
          yPercent: 100,
          ease: "expo.out",
          duration: 2,
        },
        "-=1.5"
      );

    // Dönen Kareler
    const gsapSq = gsap.utils.toArray(".section-title__square");
    gsapSq.forEach((gSq) => {
      const rotateAnim = gsap.from(gSq, {
        rotation: 720,
        duration: 3,
      });
      ScrollTrigger.create({
        trigger: gSq,
        animation: rotateAnim,
        start: "top bottom",
        scrub: 1.9,
      });
    });

    // Animasyon Fonksiyonları
    const createScrollAnimation = (target, props, trigger, extraProps = {}) => {
      gsap.to(target, {
        scrollTrigger: {
          trigger: trigger,
          start: "top top",
          scrub: 1.2,
          ...extraProps,
        },
        ...props,
      });
    };

    const headerAnimations = () => {
      createScrollAnimation(".title_paralax", { yPercent: -150 }, ".header");
      createScrollAnimation(".title__exp", { xPercent: -150 }, ".header");
      createScrollAnimation(".header__btn", { xPercent: -150 }, ".header");
      createScrollAnimation(".header .stroke", { xPercent: 50 }, ".header");
      createScrollAnimation(".header__img", { xPercent: -70 }, ".header");
      createScrollAnimation(".header__img img", { scale: 1.8 }, ".header");
      createScrollAnimation(
        ".header__marq-wrapp",
        { xPercent: -150 },
        ".header"
      );
      createScrollAnimation(
        ".header__marq-star img",
        { rotate: -720 },
        ".header"
      );
    };

    const aboutAnimations = () => {
      createScrollAnimation(".about__img", { yPercent: 30 }, ".about", {
        start: "top bottom",
      });
      createScrollAnimation(".about__img img", { scale: 1.4 }, ".about", {
        start: "top bottom",
      });
      createScrollAnimation(".about__txt", { yPercent: 30 }, ".about__wrapp", {
        start: "top bottom",
      });
    };

    const benefitsAnimations = () => {
      gsap.from(".benefits__num", {
        x: (i, el) => 1 - parseFloat(el.getAttribute("data-speed")),
        scrollTrigger: {
          trigger: ".benefits__list",
          start: "top bottom",
          scrub: 1.9,
        },
      });
    };

    const portfolioAnimations = () => {
      gsap.from([".work__item", ".work__item-num"], {
        y: (i, el) => 1 - parseFloat(el.getAttribute("data-speed")),
        scrollTrigger: {
          trigger: ".work",
          start: "top bottom",
          scrub: 1.9,
        },
      });
      createScrollAnimation(
        ".work__item-img img",
        { scale: 1.6 },
        ".work__wrapp",
        { start: "top bottom" }
      );
      createScrollAnimation(
        ".section-title",
        { xPercent: 100 },
        ".work__wrapp",
        {
          start: "top bottom",
        }
      );
      createScrollAnimation(
        ".hub-exp-div",
        { xPercent: -120 },
        ".work__wrapp",
        {
          start: "top bottom",
        }
      );
    };

    const servicesAnimations = () => {
      gsap.from(".serv__item-arrow", {
        x: (i, el) => 1 - parseFloat(el.getAttribute("data-speed")),
        scrollTrigger: {
          trigger: ".serv__list",
          start: "top bottom",
          scrub: 1.9,
        },
      });
    };

    const footerAnimations = () => {
      gsap.from(".footer__div span", {
        y: (i, el) => 1 - parseFloat(el.getAttribute("data-speed")),
        opacity: 0,
        scrollTrigger: {
          trigger: ".footer",
          start: "top bottom",
          end: "bottom bottom",
          scrub: 1.9,
        },
      });
    };

    // Animasyonları Başlatma
    headerAnimations();
    aboutAnimations();
    benefitsAnimations();
    portfolioAnimations();
    servicesAnimations();
    footerAnimations();
  },
};
</script>
